// Helper functions
export function getServiceById(id: string) {
  return services.find((service) => service.id === id);
}

export function getCaseStudyById(id: string) {
  return caseStudies.find((study) => study.id === id);
}

export function getRelatedCaseStudies(serviceId: string) {
  const service = getServiceById(serviceId);
  if (!service) return [];
  return caseStudies.filter((study) =>
    service.caseStudies.includes(study.id)
  );
}

export function getCaseStudiesByCategory(category: string) {
  if (category === "all") return caseStudies;
  return caseStudies.filter(
    (study) => study.category === category
  );
}
